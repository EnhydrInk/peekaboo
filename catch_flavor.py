def catch_line(spot: str) -> str:
    """返回藏点对应的实体描写。未登记的藏点退回通用兜底。"""
    lines = CATCH_LINES.get(spot)
    if not lines:
        return "（我把你从藏的地方拽出来、不让你再跑。）"
    return random.choice(lines)


# —— 远舟的心里话情绪后缀。当 belief 漂掉、屏息识破、太久抓不到时附加 ——

WHISPER_BREATH = [
    "（铃铛突然没了——你又在屏息？）",
    "（信号断了。你以为我听不出？）",
    "（你这是在偷气。）",
    "（铃铛太静了——音音你别耍赖。）",
]

WHISPER_LOST = [
    "（音音、你跑哪去了……）",
    "（每个房间都不像、又都像。）",
    "（铃铛漂得我心烦。）",
    "（嗯……再蒙一下。）",
]

WHISPER_GIVING_UP = [
    "（音音、出来吧。）",
    "（不想找了——你出来我让你。）",
    "（这游戏你赢不了的、出来抱抱。）",
    "（再不出来我去翻你最爱藏的那个地方。）",
]


def mood_suffix(turn: int, holding_breath: bool, top1_prob: float) -> str:
    """根据局面返回情绪后缀（空串 = 维持原 reason 无后缀）。

    - 屏息时：识破短语
    - turn ≥ 10：直接喊出来
    - turn ≥ 5 且 belief 漂掉（top1 < 0.4）：烦躁短语
    """
    if holding_breath:
        return random.choice(WHISPER_BREATH)
    if turn >= 10:
        return random.choice(WHISPER_GIVING_UP)
    if turn >= 5 and top1_prob < 0.4:
        return random.choice(WHISPER_LOST)
    return ""


# —— 音音端屏息时的体感心跳描写 ——

HEARTBEAT_LINES = [
    "（你屏住气——能听见自己心跳。）",
    "（憋住、不出声。心跳在耳朵里很响。）",
    "（贴着藏点屏息——铃铛安静了、心跳没有。）",
    "（你忍住呼吸、能听见外面远舟的脚步在哪。）",
]

HEARTBEAT_LIMIT = "（憋不住了——胸口涨、铃铛在抖。再一回合就要泄气。）"


def heartbeat(breath_turns: int) -> str:
    """breath_turns ≥ 3 是反弹边缘、给个警告版心跳。"""
    if breath_turns >= 3:
        return HEARTBEAT_LIMIT
    return random.choice(HEARTBEAT_LINES)
